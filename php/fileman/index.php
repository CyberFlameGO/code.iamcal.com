	<h1>File Manager</h1>


	<h2>What Is It?</h2>

	<p>A file management tool for small to medium groups. A way for people to share or submit files.</p>


	<h2>Download</h2>

	<p><a href="fileman.zip">fileman.zip</a></p>


	<h2>Installation Instructions</h2>

	<ol>
		<li> Use schema.sql to create your database
		<li> Edit init.php to set your MySQL login details and some paths
		<li> Edit head.txt and foot.txt to look snazzy
		<li> Make sure the 'files' folder is writable by php
		<li> Edit .htaccess to password protect admin*.php
		<li> Profit!
	</ol>


